            ->where('deleted_at',null)
            ->orderBy('order', 'asc')
            ->orderBy('arabic_name', 'asc')
            ->paginate(8);
        if(!$allBrands){
            return response()->json(['message' => 'BRANDS NOT FOUND'], 404);
        }
        return response()->json([
            'brands'=>$allBrands
